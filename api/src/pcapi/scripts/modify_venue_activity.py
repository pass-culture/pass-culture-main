BATCH_SIZE = 100


def check_venue_names() -> None:
    venues = Venue.query.filter(Venue.id.in_(VENUES_TO_CHANGE.keys())).all()
    for venue in venues:
        if venue.name != VENUES_TO_CHANGE[venue.id]:
            print(f"Venue {venue.id} name is {venue.name} but should be {VENUES_TO_CHANGE[venue.id]}")


def update_venue_main_activity(dry_run: bool = True) -> None:
    venue_ids = list(VENUES_TO_CHANGE.keys())
    for i in range(0, len(venue_ids), BATCH_SIZE):
        venues = Venue.query.filter(Venue.id.in_(venue_ids[i : i + BATCH_SIZE])).all()
        for venue in venues:
            if venue.venueTypeCode != VenueTypeCode.DISTRIBUTION_STORE:
                print(f"Venue {venue.id}: {venue.name} main activity is {venue.venueTypeCode} and will be updated")
                if not dry_run:
                    venue.venueTypeCode = VenueTypeCode.DISTRIBUTION_STORE
                    db.session.add(venue)
        if not dry_run:
            db.session.commit()
