logger = logging.getLogger(__name__)


def main(not_dry: bool) -> None:
    user_ids = _get_suspended_users_id()

    should_continue_users = []
    activated_users = []
    not_eligible_users = []
    waiting_for_id_check_users = []
    rejected_users = []
    in_error_users = []
    already_handled_users = []

    for user_id in user_ids:
        user = users_models.User.query.filter(users_models.User.id == user_id).one_or_none()
        if not user:
            logger.warning("User %s not found", user_id)
            in_error_users.append((user_id, "User not found"))
            continue

        if user.isActive:
            logger.info("User %s is active, skipping", user_id)
            already_handled_users.append(user_id)
            continue

        if not user.deposit or user.deposit.type != finance_models.DepositType.GRANT_17_18:
            logger.info("User %s has no grant 17-18 deposit, skipping", user_id)
            in_error_users.append((user_id, "User has no grant 17-18 deposit"))
            continue

        has_removed_deposit, reason = _remove_deposit_from_user(user)
        if not has_removed_deposit:
            in_error_users.append((user_id, f"Could not remove deposit: {reason}"))
            continue
        user_subscription_state = subscription_api.get_user_subscription_state(user)

        if user_subscription_state.next_step is not None:
            # The user can complete the subscription
            logger.info("User %s can continue activation", user.id)
            user.isActive = True
            should_continue_users.append((user_id, user.email))
            continue

        # No next step.
        if user_subscription_state.is_activable:
            activated, reason = _handle_activable_user(user, not_dry)
            if activated:
                activated_users.append((user_id, user.email))
                user.isActive = True
            else:
                in_error_users.append((user_id, reason))
            continue

        # Not activable and no nex step can mean
        # - Not eligible
        # - Waiting for identity validation
        if user_subscription_state.young_status.status_type != YoungStatusType.ELIGIBLE:  # type: ignore[attr-defined]
            logger.info("User %s is not eligible", user.id)
            not_eligible_users.append((user_id, user.email))
            user.isActive = True
            continue

        if user_subscription_state.identity_fraud_check and user_subscription_state.identity_fraud_check.status in [
            FraudCheckStatus.STARTED,
            FraudCheckStatus.PENDING,
        ]:
            logger.info("User %s is waiting for identity validation", user.id)
            waiting_for_id_check_users.append((user_id, user.email))
            user.isActive = True
            continue

        if user_subscription_state.fraud_status in [SubscriptionItemStatus.KO, SubscriptionItemStatus.SUSPICIOUS]:
            logger.info("User %s has been rejected", user.id)
            user.isActive = True
            rejected_users.append((user_id, user.email))
            continue

        # failing all that, log the user
        serialized_status_state: dict[str, typing.Any] = {}
        if user_subscription_state.fraud_status:
            serialized_status_state["fraud_status"] = user_subscription_state.fraud_status.value
        if user_subscription_state.next_step:
            serialized_status_state["next_step"] = user_subscription_state.next_step.value
        if user_subscription_state.young_status:
            serialized_status_state["young_status"] = {
                "subscription_status": user_subscription_state.young_status.subscription_status.value,  # type: ignore [attr-defined]
                "status_type": user_subscription_state.young_status.status_type.value,  # type: ignore [attr-defined]
            }
        if user_subscription_state.identity_fraud_check:
            serialized_status_state["identity_fraud_check"] = user_subscription_state.identity_fraud_check.id
        if user_subscription_state.is_activable:
            serialized_status_state["is_activable"] = user_subscription_state.is_activable
        if user_subscription_state.subscription_message:
            serialized_status_state["subscription_message"] = user_subscription_state.subscription_message.user_message

        _log_avoiding_max_chars(f"User {user.id} has not been handled. Extra: {serialized_status_state}")
        in_error_users.append((user.id, "Not handled"))

    _log_mailing_lists(
        should_continue_users, activated_users, not_eligible_users, waiting_for_id_check_users, rejected_users
    )
    _log_not_handled_users(in_error_users, already_handled_users)


def _log_mailing_lists(
    should_continue_users: list[tuple[int, str]],
    activated_users: list[tuple[int, str]],
    not_eligible_users: list[tuple[int, str]],
    waiting_for_id_check_users: list[tuple[int, str]],
    rejected_users: list[tuple[int, str]],
) -> None:
    logger.info("Mailing lists, users un-suspended")
    _log_avoiding_max_chars(f"Should continue ({len(should_continue_users)} users): {should_continue_users}")
    _log_avoiding_max_chars(f"Activated ({len(activated_users)} users): {activated_users}")
    _log_avoiding_max_chars(f"Not eligible ({len(not_eligible_users)} users): {not_eligible_users}")
    _log_avoiding_max_chars(
        f"Waiting for id check ({len(waiting_for_id_check_users)} users): {waiting_for_id_check_users}"
    )
    _log_avoiding_max_chars(f"Rejected ({len(rejected_users)} users) {rejected_users}")


def _log_not_handled_users(
    in_error_users: list,
    already_handled_users: list,
) -> None:
    logger.info("Not handled users - suspension status unchanged")
    _log_avoiding_max_chars(f"In error ({len(in_error_users)} users): {in_error_users}")
    _log_avoiding_max_chars(f"Already handled ({len(already_handled_users)} users): {already_handled_users}")


def _log_avoiding_max_chars(to_log: str) -> None:
    # logs are truncated after 1024 chars
    # The first 196 chars are always : `{"api_key_offerer_id": null, "api_key_provider_id": null, "logging.googleapis.com/trace": "xxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxx", "module": "__main__", "severity": "INFO", "user_id": null, "message": `
    # the last 40 are: `"technical_message_id": "", "extra": {})`
    # This leaves us with 788 chars to log a message and any extra data. I keep a few extra chars just in case
    if len(to_log) <= 700:
        logger.info(to_log)
        return

    parts = len(to_log) // 700
    for i in range(parts):
        logger.info("Part %s/%s: %s", i + 1, parts, to_log[i * 700 : (i + 1) * 700])


def _remove_deposit_from_user(user: users_models.User) -> tuple[bool, str]:
    if not user.deposit:
        logger.error("User %s has no deposit to remove", user.id)
        return False, "No deposit on user (OK)"

    deposit_to_remove = finance_models.Deposit.query.filter(
        finance_models.Deposit.userId == user.id, finance_models.Deposit.type == finance_models.DepositType.GRANT_17_18
    ).one_or_none()
    if len(deposit_to_remove.bookings) > 0:
        logger.error(
            "Could not delete user %s deposit (%s), existing bookings (%s bookings)",
            user.id,
            deposit_to_remove.id,
            len(deposit_to_remove.bookings),
        )
        return False, "Bookings attached to deposit (KO)"
    recredits = deposit_to_remove.recredits

    for recredit in recredits:
        db.session.delete(recredit)
    db.session.delete(deposit_to_remove)

    underage_deposit = finance_models.Deposit.query.filter(
        finance_models.Deposit.userId == user.id, finance_models.Deposit.type == finance_models.DepositType.GRANT_15_17
    ).one_or_none()

    # One user has an ok GRANT_18 deposit, do not remove their role
    if user.deposit.type != finance_models.DepositType.GRANT_18:
        user.remove_beneficiary_role()
        if underage_deposit:
            user.add_underage_beneficiary_role()

    db.session.refresh(user)
    return True, "OK"


def _handle_activable_user(user: users_models.User, not_dry: bool) -> tuple[bool, str]:
    if not not_dry:
        logger.info("Would try to activate user %s", user.id)
        return True, ""
    try:
        activated = subscription_api.activate_beneficiary_if_no_missing_step(user)
    except Exception as e:  # pylint: disable=broad-except
        logger.error("Error while activating user %s: %s. Carrying on.", user.id, e)
        return False, str(e)
    if not activated:
        if user.is_beneficiary:
            logger.info("User %s is already activated", user.id)
            return True, "already beneficiary"

        logger.info("User %s could not be activated", user.id)
        return False, "Not activated: unknown reason"

    logger.info("Successfully activated user %s", user.id)
    return True, ""


